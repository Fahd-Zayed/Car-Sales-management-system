    void save(String user, String newPass, String email) throws IOException {
        File f = new File("sign_up.txt");
        FileWriter fss = new FileWriter(f, true);
        try (PrintWriter p = new PrintWriter(fss)) {
            p.println("," + user + "," + newPass + "," + email + "");
        }
    }

