    private void btn_signupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_signupActionPerformed
        try {
            String email = ed_email.getText().toLowerCase();
            String userame = ed_username.getText();
            char[] password = ed_password.getPassword();
            String switchedPass = String.valueOf(password);
            save(userame, switchedPass, email);
        } catch (IOException ex) {
            Logger.getLogger(Sign_Up.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(this, "Sign up successfully");
        new_signup();
    }//GEN-LAST:event_btn_signupActionPerformed

